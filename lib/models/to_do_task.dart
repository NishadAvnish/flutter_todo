class ToDoTask{

  final String id;
  final String title;
  final String subTitle;
  bool isComplete;
  final String detail;
  final String imageUrl;
  final DateTime dateAdded;
  final DateTime deadLine;

  ToDoTask({
    this.id,
    this.title,
    this.subTitle,
    this.isComplete,
    this.detail,
    this.imageUrl,
    this.dateAdded,
    this.deadLine,
  });

}